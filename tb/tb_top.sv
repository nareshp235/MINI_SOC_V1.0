`timescale 1ns/1ps

module tb_top;

    import params_pkg::*;

    //--------------------------------------------------------------------------
    // Clock / Reset
    //--------------------------------------------------------------------------

    logic pclk;
    logic presetn;

    //--------------------------------------------------------------------------
    // GPIO Signals
    //--------------------------------------------------------------------------

    logic [7:0] gpio_in;
    logic [7:0] gpio_out;
    logic       gpio_irq;

    //--------------------------------------------------------------------------
    // APB Interface
    //--------------------------------------------------------------------------

    apb_if apb
    (
        .pclk    (pclk),
        .presetn (presetn)
    );

    //--------------------------------------------------------------------------
    // DUT
    //--------------------------------------------------------------------------

    gpio_apb u_gpio
    (
        .apb      (apb),

        .gpio_in  (gpio_in),
        .gpio_out (gpio_out),

        .gpio_irq (gpio_irq)
    );

    //--------------------------------------------------------------------------
    // BFM Handle
    //--------------------------------------------------------------------------

    apb_master_bfm bfm;

    //--------------------------------------------------------------------------
    // Clock Generation
    //--------------------------------------------------------------------------

    initial
    begin
        pclk = 0;

        forever #5 pclk = ~pclk;
    end

    //--------------------------------------------------------------------------
    // Reset Generation
    //--------------------------------------------------------------------------

    task apply_reset();

        presetn = 0;

        repeat(5)
            @(posedge pclk);

        presetn = 1;

        repeat(2)
            @(posedge pclk);

    endtask

    //--------------------------------------------------------------------------
    // Main Test
    //--------------------------------------------------------------------------

    logic [31:0] rd_data;

    initial
    begin

        $display("");
        $display("========================================");
        $display(" Mini SoC GPIO Bringup Test");
        $display("========================================");
        $display("");

        //--------------------------------------------------------------
        // Default Inputs
        //--------------------------------------------------------------

        gpio_in = 8'h00;

        //--------------------------------------------------------------
        // Create BFM
        //--------------------------------------------------------------

        bfm = new(apb);

        //--------------------------------------------------------------
        // Initialize Bus
        //--------------------------------------------------------------

        bfm.init();

        //--------------------------------------------------------------
        // Apply Reset
        //--------------------------------------------------------------

        apply_reset();

        //--------------------------------------------------------------
        // GPIO_001
        //--------------------------------------------------------------

        $display("[TEST] GPIO_CTRL Write/Read");

        bfm.apb_write(
            GPIO_CTRL_OFFSET,
            32'h0000_0003
        );

        bfm.apb_read(
            GPIO_CTRL_OFFSET,
            rd_data
        );

        //--------------------------------------------------------------
        // Check
        //--------------------------------------------------------------

        if (rd_data == 32'h0000_0003)
        begin
            $display("[PASS] GPIO_CTRL Readback = 0x%08h",
                     rd_data);
        end
        else
        begin
            $error("[FAIL] Expected 0x00000003 Got 0x%08h",
                   rd_data);
        end

        //--------------------------------------------------------------
        // Finish
        //--------------------------------------------------------------

        repeat(10)
            @(posedge pclk);

        $display("");
        $display("Simulation Completed");
        $display("");

        $finish;

    end

endmodule : tb_top

